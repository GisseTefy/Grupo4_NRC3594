    private void llenarCampos(Empleado empleado) {
        jLabelTitulo.setText("Datos Empleado");
        jTextFieldApellidos.setText(empleado.getApellidos());
        jTextFieldNombres.setText(empleado.getNombres());
        jTextFieldCedula.setText(empleado.getCedula());
        jTextFieldCorreo.setText(empleado.getCorreo());
        jTextFieldTelef.setText(empleado.getTelefono());
        jTextFieldDireccion.setText(empleado.getDireccion());
        try {
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            Date fecha = formato.parse(empleado.getFechaNac());
            jDateChooserCalendario.setDate(fecha);
        } catch (ParseException ex) {
            Logger.getLogger(CRUEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextFieldUsuario.setText(empleado.getUsuario());
        jTextFieldPassword.setText(empleado.getPassword());
        jComboBoxDepartamento.setSelectedItem(empleado.getDepartamento().getNombreDep());
        jComboBoxPuestoT.setSelectedItem(empleado.getPuestoTrabajo().getCodigoPuesTra());
    }

    public void inicializarComboBox() {
        String[] departamento = crud.seleccionardatos("departamento", "DEP_NOMBRE");
        String[] puestoT = crud.seleccionardatos("puesto_trabajo", "PDT_NOMBRE");
        for (int i = 0; i < departamento.length; i++) {
            jComboBoxDepartamento.addItem(departamento[i]);
        }
        for (int i = 0; i < puestoT.length; i++) {
            jComboBoxPuestoT.addItem(puestoT[i]);
        }
    }

