    private void jButtonRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistarActionPerformed
        //Boton Registrar

        if (!validacion.validarApenNomb(jTextFieldApellidos.getText())) {
            empleado.setApellidos(jTextFieldApellidos.getText());
        } else {
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (!validacion.validarApenNomb(jTextFieldNombres.getText())) {
            empleado.setNombres(jTextFieldNombres.getText());
        } else {
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (!validacion.validarCedula(jTextFieldCedula.getText())) {
            empleado.setCedula(jTextFieldCedula.getText());
        } else {
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (!validacion.validarCorreo(jTextFieldCorreo.getText())) {
            empleado.setCorreo(jTextFieldCorreo.getText());
        } else {
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (!validacion.validarTelf(jTextFieldTelef.getText())) {
            empleado.setTelefono(jTextFieldTelef.getText());
        } else {
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (jDateChooserCalendario.getDate() != null) {
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/YYYY");
            String fechaN = formatoFecha.format(jDateChooserCalendario.getDate());
            empleado.setFechaNac(fechaN);
        } else {
            validacion.setMensajeE("ERROR!. El campo Fecha de Nacimiento no se encuentra llenado.");
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (!validacion.camposVacios(jTextFieldDireccion.getText())) {
            empleado.setDireccion(jTextFieldDireccion.getText());
        } else {
            validacion.setMensajeE("ERROR!. El campo Direcci√≥n no se encuetra llenado.");
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (!validacion.camposVacios(jTextFieldUsuario.getText())) {
            empleado.setUsuario(jTextFieldUsuario.getText());
        } else {
            validacion.setMensajeE("ERROR!. El campo Usuario no se encuentra llenado.");
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }
        if (!validacion.camposVacios(jTextFieldPassword.getText())) {
            empleado.setPassword(jTextFieldPassword.getText());
        } else {
            validacion.setMensajeE("ERROR!. El campo Password no se encuentra llenado.");
            mensaje.interError();
            mensaje.mostrarM("ERROR EN EL REGISTRO", validacion.getMensajeE());
            return;
        }

        crud.buscarDepartamento(departamento, "DEP_NOMBRE", jComboBoxDepartamento.getSelectedItem().toString());
        empleado.setDepartamento(departamento);
        crud.buscarPuestoT(puestoTrabajo, "PDT_NOMBRE", jComboBoxPuestoT.getSelectedItem().toString());
        empleado.setPuestoTrabajo(puestoTrabajo);

        if (jButtonRegistar.getText().equals("Registrar")) {
            empleado.setCodigoEmpleado("EMP" + crud.obtenerCodigo("empleado"));
            crud.registrarEmpleado(empleado);
            mensaje.setExito(true);
            mensaje.redicListadoEmp();
            mensaje.interExito();
            mensaje.mostrarM("Registro EXITOSO", "El registro se realiz√≥ exitosamente");
            this.setVisible(false);
        }else if (jButtonRegistar.getText().equals("Actualizar")){
            crud.actualizarEmpleado(empleado);
            mensaje.setExito(true);
            mensaje.redicListadoEmp();
            mensaje.interExito();
            mensaje.mostrarM("Actualizacion EXITOSO", "La actualizacion se realiz√≥ exitosamente");
            this.setVisible(false);
        }
        // Sistema sistema = new Sistema();
        // sistema.setVisible(true);
    }//GEN-LAST:event_jButtonRegistarActionPerformed

